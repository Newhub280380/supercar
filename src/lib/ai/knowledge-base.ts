export const getProcedureRecommendations = (
  skinType: SkinType,
  concerns: string[],
): Procedure[] => {
  return procedures.filter((p) => {
    const matchesSkin = p.suitableFor.includes(skinType);
    const matchesConcerns =
      concerns.length === 0 ||
      concerns.some(
        (c) =>
          p.indications.some((ind) =>
            ind.toLowerCase().includes(c.toLowerCase()),
          ) ||
          p.description.toLowerCase().includes(c.toLowerCase()),
      );
    return matchesSkin && matchesConcerns;
  });
};

export const getProceduresByCategory = (category: Procedure["category"]) =>
  procedures.filter((p) => p.category === category);

export const searchProcedures = (query: string) => {
  const q = query.toLowerCase();
  return procedures.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q) ||
      p.indications.some((i) => i.toLowerCase().includes(q)) ||
      p.category.toLowerCase().includes(q),
  );
};

export const searchFAQ = (query: string) => {
  const q = query.toLowerCase();
  return faqItems.filter(
    (item) =>
      item.question.toLowerCase().includes(q) ||
      item.answer.toLowerCase().includes(q) ||
      item.category.toLowerCase().includes(q),
  );
};

